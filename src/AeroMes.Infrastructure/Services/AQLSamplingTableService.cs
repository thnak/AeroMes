    public AQLSamplingPlan GetSamplingPlan(int lotSize, string aqlLevel, string inspectionLevel)
    {
        var entry = _letterCodes
            .Where(x => x.Level == inspectionLevel && lotSize >= x.Min && lotSize <= x.Max)
            .Select(x => x.Code)
            .FirstOrDefault();

        if (entry is null)
            throw new ArgumentException($"No sampling plan for lot size {lotSize}, inspection level '{inspectionLevel}'.");

        if (!_plans.TryGetValue((entry, aqlLevel), out var plan))
            throw new ArgumentException($"No AQL plan for code '{entry}', AQL '{aqlLevel}'.");

        return new AQLSamplingPlan(plan.N, plan.Ac, plan.Re);
    }
